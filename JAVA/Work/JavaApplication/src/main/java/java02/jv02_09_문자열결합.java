package java02;

public class jv02_09_문자열결합 {
    
    public static void main(String[] args) {
        
        int i = 10, j = 20;
        int sum = i + j;
        
        System.out.println("더하기 : " + i + " + " + j + " = " + sum);
        
        String s1 = "Hello";
        String s2 = " World";
        System.out.println(s1 + s2);
        
        String s3 = s1 + s2;
        System.out.println(s3);
    }
    
}
