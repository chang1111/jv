package java02;

public class jv02_01_변수선언 {
    
    public static void main(String[] args) {
        
        boolean b = true;
        char c = 'a';
        int i = 0;
        double t = 0.05;
        
        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
        System.out.println(t);
        
        b = false;
        c = 'A';
        i = 100;
        t = 20.17;
        
        System.out.println(b);
        System.out.println(c);
        System.out.println(i);
        System.out.println(t);
    }
    
}
